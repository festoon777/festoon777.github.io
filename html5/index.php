                  		<h2>&nbsp;&nbsp;Packing for your hospital stay</h2>
                        
                        
                        
                    
                        
                        <p class="text">
                       As you approach week 35 in your pregnancy, it's a good idea to be prepared by packing your bag for the hospital and putting it in the car. Most of what you bring will depend on your personal preferences and comforts, in addition to your identification, health insurance card and a car seat for baby's first trip home.
                        </p>
                        
                     
                        <div class="clear"></div>
                        
                        <h2>&nbsp;&nbsp;Some Suggestions:</h2>
                        
                        <div class="li_items">
                        
                        	<ul>
                            
                           		<li> Personal identification and health insurance card</li>
                            	<li> List of phone numbers and e-mail addresses for people you want to contact after the delivery</li>
                            	<li> Lip balm or Chap Stick® (your lips get dry during labor)</li>
                            	<li> A watch with a second hand to time contractions</li>
                            	<li> Toothbrush and toothpaste</li>
                            	<li> Barrettes or band to tie back long hair</li>
                            	<li> Pillow from home with a pillowcase other than white (to distinguish your pillow from the hospital's)</li>
                            	<li> Music or CDs</li>
                            	<li> A going-home outfit for you and baby</li>
                            	<li> Blanket for baby</li>
                            	<li> A nursing gown and/or comfortable robe that opens all the way down the front</li>
                            	<li> Nursing bras and nursing pads</li>
                            	<li> Slippers/socks to keep your feet warm</li>
                            	<li> Personal hygiene items you will want (shampoo, brush, etc.)</li>
                            	<li> You will also need an infant car seat properly installed in your car before you are discharged from the Family Birth Center.</li>
                            </ul>
                        
